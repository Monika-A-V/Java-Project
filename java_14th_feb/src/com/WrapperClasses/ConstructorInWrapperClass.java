package com.WrapperClasses;

public class ConstructorInWrapperClass {

	public static void main(String[] args) {

		        //Integer class constructors
				Integer i1=new Integer(23);
				Integer i2=new Integer("2345");
				System.out.println("object i1 : "+i1);
				System.out.println("object i2 : "+i2);
				
				
				//float constructors
				Float f1=new Float(43.3f);
				Float f2=new Float(4323.43);
				Float f3=new Float(34.56d);
				System.out.println("object f1 : "+f1);
				System.out.println("object f2 : "+f2);
				System.out.println("object f3 : "+f3);
				

		        //character constructors
				Character ch=new Character('G');
				System.out.println("object ch : "+ch);

			}}