package com.Test_Loop_1;

public class Loop_12 
{

	public static void main(String[] args)
	{
		        for (int i = 1; i <= 50; i++) 
		        {
		            if (i % 2 == 0)
		            {
		                System.out.println(i);
		            } else
		            {
		                int negativeValue = i * -1;
		                int squaredValue = i * i;
		                System.out.println(negativeValue + " " + squaredValue);
		            }
		        }
		    }}
		




